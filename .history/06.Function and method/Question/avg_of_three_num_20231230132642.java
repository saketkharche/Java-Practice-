
public class avg_of_three_num {
    public static void avg_of_three_num(int n) {
        int a = 15;
        int b = 65;
        int c = 32;

        int avg = a + b + c / 3;
        System.out.println("The average of " + n + " numbers is: " + avg);
        return;
    }

    public static void main(String[] args) {
        avg_of_three_num(52);
    }
}
