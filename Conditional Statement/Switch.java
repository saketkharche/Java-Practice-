public class Switch {
    public static void main(String[] args) {
        int number = 2;
        switch (number) {
            case 1:
                System.out.println("samosa");
            case 2:
                System.out.println("burger");
            case 3:
                System.out.println("mango shake");
            default:
                System.out.println("wake up to reality");

        }
    }
}
